async def generate_email(state: EmailApprovalState) -> EmailApprovalState:
    """
    Generate email body based on recipient and subject.
    
    This is the FIRST node in the workflow. After this completes,
    the workflow pauses at the interrupt_before=["send_email"] checkpoint.
    """
    logger.info(f"[generate_email] Starting for recipient: {state['recipient']}")
    
    try:
        # Validate input
        email_error = validate_email(state)
        if email_error:
            logger.error(f"[generate_email] Validation failed: {email_error}")
            state["error_message"] = email_error
            return state
        
        subject_error = validate_subject(state)
        if subject_error:
            logger.error(f"[generate_email] Validation failed: {subject_error}")
            state["error_message"] = subject_error
            return state
        
        # Simulate LLM call to generate email body
        logger.info(f"[generate_email] Generating body for subject: {state['subject']}")
        await asyncio.sleep(0.5)  # Simulate LLM latency
        
        # Generated email body (simulated)
        state["body"] = f"""Dear {state['recipient'].split('@')[0]},

{state['subject']}

Thank you for reaching out. We appreciate your inquiry and would like to provide you with the following information:

Key Points:
1. We understand your concern about the recent updates.
2. Our team has reviewed the feedback you provided.
3. We are committed to addressing all issues promptly.

Next Steps:
- A dedicated specialist will contact you within 24 hours
- We will provide detailed documentation of changes
- You will receive follow-up support as needed

Best regards,
Customer Service Team
AI Architecture Blueprints"""
        
        state["generated_at"] = datetime.now().isoformat()
        state["error_message"] = None
        state["step_history"] = state.get("step_history", [])
        state["step_history"].append({
            "step": "generate_email",
            "timestamp": state["generated_at"],
            "status": "success"
        })
        
        logger.info(f"[generate_email] Email body generated successfully")
        logger.info(f"[generate_email] Workflow PAUSED at interrupt checkpoint")
        
    except Exception as e:
        logger.error(f"[generate_email] Unexpected error: {e}")
        state["error_message"] = f"Generation failed: {str(e)}"
        state["step_history"].append({
            "step": "generate_email",
            "timestamp": datetime.now().isoformat(),
            "status": "error",
            "error": str(e)
        })
    
    return state


async def approval_check(state: EmailApprovalState) -> EmailApprovalState:
    """
    Route based on approval status.
    
    This node checks the approval status field (set by human during interrupt).
    Returns the next node to execute based on approval decision.
    """
    logger.info(f"[approval_check] Evaluating approval status: {state.get('approval_status')}")
    
    status = state.get("approval_status")
    
    if status == "approved":
        logger.info(f"[approval_check] Email APPROVED, proceeding to send")
        state["reviewed_at"] = datetime.now().isoformat()
    elif status == "rejected":
        logger.info(f"[approval_check] Email REJECTED")
        state["error_message"] = f"Email rejected by reviewer: {state.get('approval_notes', 'No reason provided')}"
        state["reviewed_at"] = datetime.now().isoformat()
    else:
        logger.warning(f"[approval_check] Invalid approval status: {status}")
        state["error_message"] = "Approval status must be 'approved' or 'rejected'"
    
    return state


def route_after_approval(state: EmailApprovalState) -> str:
    """
    Conditional routing after approval check.
    
    Returns the next node name based on approval status.
    """
    status = state.get("approval_status")
    
    if status == "approved":
        return "send_email"
    else:
        # Rejected or invalid status
        return END


async def send_email(state: EmailApprovalState) -> EmailApprovalState:
    """
    Send email to recipient.
    
    IMPORTANT: This node only executes if approval_status == "approved".
    This is enforced by the approval_check node and routing logic.
    """
    logger.info(f"[send_email] Attempting to send email to {state['recipient']}")
    
    try:
        # Double-check approval (defensive programming)
        if state.get("approval_status") != "approved":
            state["error_message"] = "Cannot send: email not approved"
            logger.error(f"[send_email] Email not approved, skipping send")
            return state
        
        # Validate email body exists
        if not state.get("body"):
            state["error_message"] = "Email body is empty"
            logger.error(f"[send_email] Email body is empty")
            return state
        
        # Simulate sending (with retry logic)
        max_retries = 3
        for attempt in range(1, max_retries + 1):
            try:
                logger.info(f"[send_email] Send attempt {attempt}/{max_retries}")
                await asyncio.sleep(0.3)  # Simulate email service latency
                
                # Simulate occasional failures
                if attempt < 2 and asyncio.random() > 0.7:
                    raise ExecutionError("Simulated email service error")
                
                # Success
                state["send_timestamp"] = datetime.now().isoformat()
                state["error_message"] = None
                state["step_history"].append({
                    "step": "send_email",
                    "timestamp": state["send_timestamp"],
                    "status": "success",
                    "attempt": attempt
                })
                
                logger.info(f"[send_email] Email sent successfully at {state['send_timestamp']}")
                return state
                
            except Exception as e:
                logger.warning(f"[send_email] Attempt {attempt} failed: {e}")
                
                if attempt < max_retries:
                    # Exponential backoff
                    wait_time = 0.5 * (2 ** (attempt - 1))
                    await asyncio.sleep(wait_time)
                else:
                    # All retries exhausted
                    state["error_message"] = f"Email send failed after {max_retries} attempts: {str(e)}"
                    state["step_history"].append({
                        "step": "send_email",
                        "timestamp": datetime.now().isoformat(),
                        "status": "error",
                        "error": state["error_message"]
                    })
                    logger.error(f"[send_email] {state['error_message']}")
                    return state
    
    except Exception as unexpected:
        logger.error(f"[send_email] Unexpected error: {unexpected}")
        state["error_message"] = f"Unexpected error: {str(unexpected)}"
        state["step_history"].append({
            "step": "send_email",
            "timestamp": datetime.now().isoformat(),
            "status": "error",
            "error": str(unexpected)
        })
    
    return state


async def log_completion(state: EmailApprovalState) -> EmailApprovalState:
    """Log workflow completion with final status."""
    final_status = "success" if state.get("send_timestamp") else "rejected"
    logger.info(f"[log_completion] Workflow completed with status: {final_status}")
    
    state["step_history"].append({
        "step": "log_completion",
        "timestamp": datetime.now().isoformat(),
        "status": final_status
    })
    
    return state


