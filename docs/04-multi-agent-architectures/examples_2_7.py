def build_email_approval_graph(
    checkpoint_path: str = None
) -> any:
    """
    Build the email approval workflow graph.
    
    Args:
        checkpoint_path: Deprecated parameter (kept for compatibility)
    
    Returns:
        Compiled graph with interrupts configured
    
    Note:
        In production, use:
        from langgraph.checkpoint.sqlite import SqliteSaver
        checkpointer = SqliteSaver(conn=sqlite3.connect("checkpoints.db"))
    """
    logger.info("Building email approval workflow graph...")
    
    # Create state graph
    graph = StateGraph(EmailApprovalState)
    
    # Add nodes
    logger.info("Adding nodes...")
    graph.add_node("generate_email", generate_email)
    graph.add_node("approval_check", approval_check)
    graph.add_node("send_email", send_email)
    graph.add_node("log_completion", log_completion)
    
    # Add edges
    logger.info("Adding edges...")
    graph.add_edge(START, "generate_email")
    graph.add_edge("generate_email", "approval_check")
    graph.add_conditional_edges(
        "approval_check",
        route_after_approval,
        {"send_email": "send_email", END: END}
    )
    graph.add_edge("send_email", "log_completion")
    graph.add_edge("log_completion", END)
    
    # Create checkpointer for state persistence
    logger.info("Creating in-memory checkpointer for demonstration")
    checkpointer = MemorySaver()
    
    # Compile graph with checkpointer and interrupts
    logger.info("Compiling graph with checkpointer and interrupts...")
    compiled_graph = graph.compile(
        checkpointer=checkpointer,
        interrupt_before=["send_email"]  # Pause BEFORE send_email node
    )
    
    logger.info("Graph compiled successfully")
    return compiled_graph


