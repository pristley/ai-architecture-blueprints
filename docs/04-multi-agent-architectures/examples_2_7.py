"""
LangGraph Checkpointing & Human-in-the-Loop: Email Approval Workflow (WP-2.7)

Demonstrates how LangGraph's checkpointer enables:
1. Automatic state persistence at breakpoints
2. Human approval gates before critical actions
3. State resumption and branching (approve/reject/modify)
4. Audit trails and observability

Workflow:
  generate_email → [INTERRUPT] → approval_check → send_email
                        ↓                         ↓
                   (User Reviews)            (Complete)
"""

import asyncio
import logging
import os
from dataclasses import dataclass, asdict
from datetime import datetime
from typing import Dict, List, Optional
from typing_extensions import TypedDict

# LangChain & LangGraph imports
from langgraph.graph import StateGraph, START, END
from langgraph.checkpoint.memory import MemorySaver
from langchain_core.runnables import RunnableConfig

# Configure logging
logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s"
)
logger = logging.getLogger(__name__)


# ============================================================================
# STATE DEFINITIONS
# ============================================================================

class EmailApprovalState(TypedDict):
    """
    State for email approval workflow.
    
    Fields progress through:
    - recipient, subject: Input provided by user
    - body: Generated by LLM (generated_email node)
    - approval_status: Set by human reviewer (None → approved/rejected)
    - send_timestamp: Set when email sent
    - error_message: Set if any step fails
    """
    # Input fields
    recipient: str
    subject: str
    
    # Generated content
    body: Optional[str]
    generated_at: Optional[str]
    
    # Approval fields
    approval_status: Optional[str]  # "pending" | "approved" | "rejected"
    approval_notes: Optional[str]
    reviewed_at: Optional[str]
    
    # Output fields
    send_timestamp: Optional[str]
    error_message: Optional[str]
    
    # Metadata
    thread_id: Optional[str]
    step_history: List[Dict]


# ============================================================================
# ERROR DEFINITIONS
# ============================================================================

class EmailApprovalError(Exception):
    """Base exception for email approval workflow."""
    pass


class ValidationError(EmailApprovalError):
    """Raised when input validation fails."""
    pass


class ExecutionError(EmailApprovalError):
    """Raised when node execution fails."""
    pass


# ============================================================================
# VALIDATION FUNCTIONS
# ============================================================================

def validate_email(state: EmailApprovalState) -> Optional[str]:
    """
    Validate email address format.
    
    Returns error message if invalid, None if valid.
    """
    recipient = state.get("recipient", "")
    
    if not recipient:
        return "Recipient field is empty"
    
    if "@" not in recipient or "." not in recipient:
        return f"Invalid email format: {recipient}"
    
    if len(recipient) > 254:
        return "Email address too long"
    
    return None


def validate_subject(state: EmailApprovalState) -> Optional[str]:
    """Validate subject line."""
    subject = state.get("subject", "")
    
    if not subject:
        return "Subject field is empty"
    
    if len(subject) > 1000:
        return "Subject too long"
    
    return None


