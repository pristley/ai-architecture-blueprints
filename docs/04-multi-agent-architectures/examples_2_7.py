async def run_approval_workflow_with_approval(
    recipient: str,
    subject: str,
    thread_id: Optional[str] = None,
    checkpoint_path: str = None
) -> Dict:
    """
    Execute email approval workflow and handle approval.
    
    Demonstrates:
    1. First execution: generate → pause at interrupt
    2. User approval via state update
    3. Second execution: resume from checkpoint → send → complete
    
    Args:
        recipient: Email address
        subject: Email subject line
        thread_id: Unique workflow execution ID
        checkpoint_path: Deprecated parameter (kept for compatibility)
    
    Returns:
        Final workflow state
    """
    if not thread_id:
        thread_id = f"email-{datetime.now().timestamp()}"
    
    logger.info("=" * 80)
    logger.info(f"WORKFLOW: Email Approval (thread_id={thread_id})")
    logger.info("=" * 80)
    
    # Build graph
    graph = build_email_approval_graph()
    
    # Configuration for checkpointer
    config = {"configurable": {"thread_id": thread_id}}
    
    # ========== STEP 1: Generate Email ==========
    logger.info("\n" + "=" * 80)
    logger.info("STEP 1: Generate Email")
    logger.info("=" * 80)
    
    initial_state = {
        "recipient": recipient,
        "subject": subject,
        "body": None,
        "generated_at": None,
        "approval_status": None,
        "approval_notes": None,
        "reviewed_at": None,
        "send_timestamp": None,
        "error_message": None,
        "thread_id": thread_id,
        "step_history": []
    }
    
    # First execution: generate → pause at interrupt
    logger.info(f"Invoking workflow with initial state...")
    result = graph.invoke(initial_state, config=config)
    
    logger.info(f"\nGenerated email body:\n{result['body']}")
    logger.info(f"\n⏸️  Workflow paused at 'send_email' interrupt")
    logger.info(f"Thread ID for resumption: {thread_id}")
    
    # ========== STEP 2: Simulate User Approval ==========
    logger.info("\n" + "=" * 80)
    logger.info("STEP 2: User Reviews and Approves")
    logger.info("=" * 80)
    
    logger.info("Simulating human review...")
    await asyncio.sleep(1)  # Simulate review time
    
    # Get current state from checkpoint
    logger.info("Retrieving state from checkpoint...")
    current_state = graph.get_state(config)
    
    # Update approval status
    logger.info("Updating approval status in state...")
    updated_values = current_state.values.copy()
    updated_values["approval_status"] = "approved"
    updated_values["approval_notes"] = "Looks good, ready to send"
    
    # Push updated state back to checkpoint
    graph.update_state(config, updated_values)
    logger.info("State updated in checkpoint")
    
    # ========== STEP 3: Resume and Send ==========
    logger.info("\n" + "=" * 80)
    logger.info("STEP 3: Resume from Checkpoint and Send")
    logger.info("=" * 80)
    
    # Resume from checkpoint (None input means "resume from last interrupt")
    logger.info("Resuming workflow from checkpoint...")
    final_result = graph.invoke(None, config=config)
    
    logger.info(f"\n✅ Email sent successfully!")
    logger.info(f"Sent at: {final_result['send_timestamp']}")
    logger.info(f"Status: {final_result.get('approval_status')}")
    
    # Log step history
    logger.info(f"\nStep History:")
    for i, step in enumerate(final_result.get("step_history", []), 1):
        logger.info(f"  {i}. {step['step']:20s} | {step['status']:10s} | {step['timestamp']}")
    
    logger.info("=" * 80)
    return final_result


